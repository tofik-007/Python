# Using the string slicing concept, you can get reverse the string. ::-1 corresponds to start:stop:step. When you pass -1 as step,
#  the start point goes to the end and stop at the front.
print("reverse of string / number ")
num = input("enetr a number/word to reverse it :")
print("reversed number/word is : ",num[::-1])
# this is called string slicing
